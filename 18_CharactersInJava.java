// Characters in Java

public class Example
{
    public static void main(String[] args) 
    {
        // Java supports unicode
        char ch1 = 97;   // 'a'
        char ch2 = 65;   // 'A'
        char ch3 = 3077;    
        System.out.println(ch1);
        System.out.println(ch2);
        System.out.println(ch3);
    }
}