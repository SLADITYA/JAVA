// In java constants are defined using final keyword

public class Example
{
    public static void main(String[] args) 
    {
        final double PI = 3.14;
        // PI = 5.14;   shows some error as we are reassigning the value of the constant
        // PI = 7.14;
        System.out.println(PI);    
    }    
}

// Naming Convection - We use UPPER_SNAKE_CASE to declare constants in Java